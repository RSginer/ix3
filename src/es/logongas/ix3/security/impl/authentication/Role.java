package es.logongas.ix3.security.impl.authentication;

import es.logongas.ix3.security.services.authorization.Permission;
import java.util.Set;

/**
 * Role de un usuario que agrupa una serie de permisos y otros roles
 * @author Lorenzo González
 */
public class Role {
    private int idRole;
    private String name;
    private Set<Permission> permissions;
    private Set<Role> roles;


    public boolean getContainPermission(Permission permission) {
        if (permissions.contains(permission)) {
            return true;
        } else {
            for(Role role:roles) {
                boolean containPermission=role.getContainPermission(permission);

                if (containPermission==true) {
                    return true;
                }
            }
        }

        return false;
    }

    /**
     * @return the idRole
     */
    public int getIdRole() {
        return idRole;
    }

    /**
     * @param idRole the idRole to set
     */
    public void setIdRole(int idRole) {
        this.idRole = idRole;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the permissions
     */
    public Set<Permission> getPermissions() {
        return permissions;
    }

    /**
     * @param permissions the permissions to set
     */
    public void setPermissions(Set<Permission> permissions) {
        this.permissions = permissions;
    }

    /**
     * @return the roles
     */
    public Set<Role> getRoles() {
        return roles;
    }

    /**
     * @param roles the roles to set
     */
    public void setRoles(Set<Role> roles) {
        this.roles = roles;
    }
}
